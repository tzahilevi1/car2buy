const SUV = /X\d|ОўОЎОњОЋ|tiggo|ОўОЋОАОЋОЪ|tucson|atto|ОљОўОЋ|gle|glc|q5|q3|q7|q8|x-?trail|ОљОДОА.?ОўОеОЎОЎОю|2008|3008|5008|kona|ОДОЋОаОћ|ehs|\bhs\b|outlander|ОљОљОЋОўОюОаОЊОе|santa|ОАОаОўОћ|kodiaq|ОДОЋОЊОЎОљОД|karoq|ОДОљОеОЋОД|rav4|cx-?\d|kamiq|kicks|ОДОЎОДОА|juke|ОўОЋОљОеОњ|touareg|tiguan|ОўОЎОњОЋОљОЪ|sportage|ОАОцОЋОеОўОњ|sorento|creta|ОДОеОўОћ|venue|nx\b|ОДОеОЋОАОљОЋОЉОе|arona|ateca|pajero|eclipse|seal.?u|song|g6|g9|d-?max|ОЊОЎОъОДОА|ОДОеОЋОА|cross|jimny|ОЋОЎОўОеОћ|vitara|ОДОЋОеОЋОюОћ ОДОеОЋОА|ix\b|ОўОеОЎОЎОю|kuga|escape|tucson/i;
const COUPE = /coupe|ОДОЋОцОћ|gran coupe|\b4-?series\b|ОАОЊОеОћ 4/i;

function parseCSV(t) {
  const rows = []; let row = [], cur = '', q = false;
  for (let i = 0; i < t.length; i++) {
    const c = t[i];
    if (q) { if (c === '"') { if (t[i + 1] === '"') { cur += '"'; i++; } else q = false; } else cur += c; }
    else { if (c === '"') q = true; else if (c === ',') { row.push(cur); cur = ''; } else if (c === '\n') { row.push(cur); rows.push(row); row = []; cur = ''; } else if (c !== '\r') cur += c; }
  }
  if (cur || row.length) { row.push(cur); rows.push(row); }
  return rows;
}
const num = (s) => { const n = parseInt(String(s || '').replace(/[^\d.]/g, ''), 10); return isNaN(n) ? 0 : n; };
const fixBrand = (s) => { s = String(s || '').replace(/&#0?39;/g, "'").trim(); return BR[s] || [s, '']; };
const fuel = (s) => { s = (s || '').trim(); if (s === 'ОЌОЕОъОюОЎ') return 'ОЌОЕОъОюОЎ'; if (s === 'ОЉОаОќОЎОЪ ОЋОЌОЕОъОюОЎ') return 'ОћОЎОЉОеОЎОЊОЎ'; if (s === 'ОЊОЎОќОю') return 'ОЊОЎОќОю'; return 'ОЉОаОќОЎОЪ'; };
const cleanUrl = (u) => { u = String(u || '').trim(); return /^https?:\/\//i.test(u) ? u : ''; };

const res = await fetch(CSV_URL, { redirect: 'follow' });
if (!res.ok) { console.error('sheet fetch failed', res.status); process.exit(1); }
const rows = parseCSV(await res.text());
const data = rows.slice(1).filter((r) => r[1] && r[1].trim());

const cars = data.filter((r) => (r[13] || '').trim() === 'ОюОъОЏОЎОеОћ').map((r) => {
  const [brand, slug] = fixBrand(r[1]);
  const model = (r[2] || '').trim();
  const trim = (r[3] || '').trim();
  const price = num(r[11]) || num(r[12]) || num(r[10]);
  const ft = fuel(r[5]);
  const hay = model + ' ' + trim;
  const gallery = [r[15], r[16], r[17]].map(cleanUrl).filter(Boolean);
  return {
    brand, name: model, trim,
    year: num(r[7]), km: num(r[6]), hand: num(r[8]) || 1,
    price, monthly: price ? Math.max(300, Math.round(price * 0.0116 / 10) * 10) : 0,
    cat: ft === 'ОЌОЕОъОюОЎ' ? 'ev' : COUPE.test(hay) ? 'sport' : SUV.test(hay) ? 'suv' : 'sedan',
    type: ft, color: (r[9] || '').trim(), plate: (r[4] || '').trim(), slug,
    img: gallery[0] || '', gallery
  };
});
cars.sort((a, b) => b.year - a.year || b.price - a.price);

const out = '/* AUTO-GENERATED by scripts/sync-used.mjs from the ОЎОЊ 2 Google Sheet. Do not edit by hand. */\n' +
  'window.Car2Buy = window.Car2Buy || {};\n' +
  'window.Car2Buy.USED = ' + JSON.stringify(cars, null, 0) + ';\n';
let prev = '';
try { prev = readFileSync(OUT, 'utf8'); } catch {}
if (prev.trim() === out.trim()) { console.log('no change (' + cars.length + ' used cars)'); process.exit(0); }
writeFileSync(OUT, out);
console.log('wrote used-cars.js Рђћ', cars.length, 'used cars');
