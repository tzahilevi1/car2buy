  'ОаОЎОАОљОЪ': ['Nissan', 'nissan'], 'ОЕОЉОеОЋОюОў': ['Chevrolet', 'chevrolet'], 'ОъОЎОдОЋОЉОЎОЕОЎ': ['Mitsubishi', 'mitsubishi'],
  'ОАОДОЋОЊОћ': ['Skoda', 'skoda'], 'ОўОЋОЎОЋОўОћ': ['Toyota', 'toyota'], 'ОљОљОЋОЊОЎ': ['Audi', 'audi'], 'ОДОЎОћ': ['Kia', 'kia'],
  'ОюОЎОаОД ОљОаОЊ ОДОЋ': ['Lynk & Co', 'lynk-co'], 'ОљОЎОАОЋОќОЋ': ['Isuzu', 'isuzu'], 'ОеОаОЋ': ['Renault', 'renault'],
  "ОњОЎ'Оц / Jeep": ['Jeep', 'jeep'], 'ОцОЋОюОДОАОЋОЋОњОЪ': ['Volkswagen', 'volkswagen'], 'ОАОЎОўОеОЋОљОЪ': ['Citroen', 'citroen'],
  "Оњ'ОЎОЎ.ОљОЎОЎ.ОАОЎ / JAC": ['JAC', 'jac'], 'ОљОДОАОцОаОњ / XPENG': ['Xpeng', 'xpeng'], 'ОАОЋОќОЋОДОЎ': ['Suzuki', 'suzuki'], 'ОЋОЋОюОЋОЋ': ['Volvo', 'volvo']
